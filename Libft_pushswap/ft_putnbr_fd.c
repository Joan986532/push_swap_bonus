#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	nb;

	if (n == INT_MIN)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= (-1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		nb = n + '0';
		write(fd, &nb, 1);
	}
}
